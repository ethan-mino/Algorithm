#include <bits/stdc++.h>
using namespace std;

#define x first
#define y second

int board[100][100];
int vis[100][100]; // 방문 표시 배열

int dx[4] = {0, -1, 0, 1};
int dy[4] = {-1, 0, 1, 0};

int shortest(queue<pair<int, int>> s, int n){    // 영역간 최단 거리를 구하는 함수
    int temp[100][100];

    for(int i = 0; i < n; i++)
        for(int j = 0; j < n; j++)
            temp[i][j] = vis[i][j];

    int v = vis[s.front().x][s.front().y];    //  해당 영역의 방문 표시 값

    while(!s.empty()){ // 각 좌표의 상,하,좌,우 좌표를 육지로 만듦.
        pair<int, int> cur = s.front(); s.pop();

        for(int dir = 0; dir < 4; dir++){
            int nx = cur.x + dx[dir];
            int ny = cur.y + dy[dir];

            if(nx < 0 || ny < 0 || nx >= n || ny >= n) continue;
            if(board[nx][ny] == 0 && temp[nx][ny] == 0){  // 바다이고, 아직 방문하지 않은 경우
                temp[nx][ny] = temp[cur.x][cur.y] + 1;
                s.push({nx, ny});
            }else if(board[nx][ny] == 1 && abs(temp[cur.x][cur.y]-temp[nx][ny]) > 10000){ // 육자이지만, 다른 영역인 경우
                return temp[cur.x][cur.y] - v;
            }
        }

    }

    return 0;
}

vector<queue<pair<int, int>>> area(int map[][100], int n){ // map의 영역 개수를 구하고, 각 영역의 좌표를 반환하는 함수
    int result = 0; // 영역의 개수
    vector<queue<pair<int, int>>> s;   // 각 영역의 좌표를 저장

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            if(map[i][j] != 0 && vis[i][j] == 0){   // 육지이고, 방문하지 않은 경우
                queue<pair<int, int>> q;    // 탐색을 위한 큐
                q.push({i, j}); // 시작 지점 push
                result++;   // 영역의 개수 증가
                vis[i][j] = result * 20000;  // 영역의 번호로 방문 표시
                
                queue<pair<int, int>> pos;

                while(!q.empty()){
                    pair<int, int> cur = q.front(); q.pop();
                    int cnt = 0;

                    for(int dir = 0; dir < 4; dir++){
                        int nx = cur.x + dx[dir];
                        int ny = cur.y + dy[dir];

                        if(nx <0 || ny < 0 || nx >= n || ny >= n) continue;
                        if(map[nx][ny] == 0) cnt++;
                        if(map[nx][ny] == 0 || vis[nx][ny] != 0) continue;  //   육지가 아니거나 이미 방문한 경우 건너뜀

                        q.push({nx, ny});
                        vis[nx][ny] = vis[cur.x][cur.y];
                    }

                    if(cnt > 0) pos.push({cur.x, cur.y});    // 상,하,좌,우에 0이 하나라도 있으면 좌표 push
                }
                s.push_back(pos);   //  해당 영역의 모든 좌표를 push
            }
        }
    }

    return s;
}

int main(){
    ios::sync_with_stdio(0);
    cin.tie(0);
    
    int n;
    cin >> n;

    for(int i = 0; i < n ; i++) // 지도를 입력 받아서 board에 저장
        for(int j = 0; j < n; j++)
            cin >> board[i][j];

    vector<queue<pair<int, int>>> areas = area(board, n);  // 초기 지도로부터 영역의 개수를 계산
    int m = 10001;

    for(int i = 0; i < areas.size(); i++){  // 각 영역을 확장시키면서 영역의 개수를 계산
        int s = shortest(areas[i], n);
        if(s < m) m = s;
    }

    cout << m << "\n";
}

